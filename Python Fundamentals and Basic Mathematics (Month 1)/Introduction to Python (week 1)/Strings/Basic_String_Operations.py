""" These are the simple things you can do with text, like finding out how long it is, combining texts, or repeating them. """

text = "Python"
print(f"Text: {text}")
print(f"Length: {len(text)}")          # How many characters?
print(f"Combining: {text + ' is fun'}") # Putting texts together
print(f"Repeating: {text * 3}")        # Repeating text