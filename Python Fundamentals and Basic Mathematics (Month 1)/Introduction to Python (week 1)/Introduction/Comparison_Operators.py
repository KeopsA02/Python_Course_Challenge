""" Operators used to compare two values and return a Boolean result (True or False). """

a = 10
b = 3
print(f"Equality: {a} == {b} -> {a == b}")           # False
print(f"Inequality: {a} != {b} -> {a != b}")         # True
print(f"Greater than: {a} > {b} -> {a > b}")         # True
print(f"Less than: {a} < {b} -> {a < b}")            # False
print(f"Greater or equal: {a} >= {b} -> {a >= b}")   # True
print(f"Less or equal: {a} <= {b} -> {a <= b}")      # False